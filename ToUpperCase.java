package JavaLearning;

public class ToUpperCase {
    public static String toUpperCase(String s) {
        return s.toUpperCase();
    }
    public static void main(String[] args) {
        System.out.println(toUpperCase("Enrico"));
        System.out.println(toUpperCase("Treutel"));
        System.out.println(toUpperCase("Brakus"));
        System.out.println(toUpperCase("steven"));
        System.out.println(toUpperCase("Steven"));
    }
}
