// 問題6：whileで10から1へ逆カウント
// 条件
//  10〜1 を逆順に表示

class A14070206 {
    public static void main(String[] args) {
        
        // Variable definition
        int num = 10;
        
        // Output
        while (num >= 1) {
            System.out.println(num);
            num--;
        }
    }
}
