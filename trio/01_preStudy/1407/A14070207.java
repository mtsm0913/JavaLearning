// 問題7：do-whileで最低1回は表示
// 条件
//  do-while を使って「Hello」を3回表示

class A14070207 {
    public static void main(String[] args) {
        
        // Variable definition
        int num = 1;
        
        // Output
        do {
            System.out.println("Hello");
            num++;
        } while(num <= 3);
    }
}
