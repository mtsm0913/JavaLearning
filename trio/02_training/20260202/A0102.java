// 問題2：計算結果を変数に入れる
// 次の条件を満たすプログラムを作りなさい。
// • int型の変数を2つ用意する（好きな数値でOK）
// • その2つを足した結果を int型の別の変数 に入れる
// • 結果を println で表示する
// ヒント：足すには「＋」演算子を使う。例・ int number = 1 + 2;

class A0102 {
    public static void main(String[] args) {
        
        // 変数定義
        int a = 111;
        int b = 222;
        
        // 計算
        int total = a + b;
        
        // 結果表示
        System.out.println(total);
    }
}
